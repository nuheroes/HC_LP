// ── 점수 → 해석 레벨 ─────────────────────────
function getLevel(score) {
  if (score >= 4.0) return "high";
  if (score >= 3.0) return "mid";
  return "low";
}

// ── 점수 계산 ─────────────────────────────────
function calcScores(answers) {
  const factorScores = FACTORS.map(f => {
    const avg = f.qs.reduce((s, i) => s + answers[i], 0) / f.qs.length;
    return { ...f, score: Math.round(avg * 100) / 100 };
  });

  const areaScores = AREAS.map(a => {
    const fs = factorScores.filter(f => f.area === a.id);
    const avg = fs.reduce((s, f) => s + f.score, 0) / fs.length;
    return { ...a, score: Math.round(avg * 100) / 100, factors: fs };
  });

  const total = Math.round(areaScores.reduce((s, a) => s + a.score, 0) / 4 * 100) / 100;

  // 강점 TOP3 / 개발포인트 BOTTOM3
  const sorted = [...factorScores].sort((a, b) => b.score - a.score);
  const strengths = sorted.slice(0, 3);
  const develops  = sorted.slice(-3).reverse();

  return { factorScores, areaScores, total, strengths, develops };
}
